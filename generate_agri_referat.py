def extract_logo() -> None:
    if LOGO_PATH.exists():
        return
    subprocess.run(
        [
            "qlmanage",
            "-t",
            "-s",
            "800",
            "-o",
            str(LOGO_PATH.parent),
            str(SVG_LOGO_PATH),
        ],
        check=True,
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
    )
    if not LOGO_PATH.exists():
        raise FileNotFoundError(f"Could not prepare logo preview from {SVG_LOGO_PATH}.")


def set_run_font(run, *, bold: bool = False, size=FONT_SIZE) -> None:
    run.font.name = FONT_NAME
    run.font.size = size
    run.bold = bold
    r_pr = run._element.get_or_add_rPr()
    r_fonts = r_pr.rFonts
    r_fonts.set(qn("w:ascii"), FONT_NAME)
    r_fonts.set(qn("w:hAnsi"), FONT_NAME)
    r_fonts.set(qn("w:cs"), FONT_NAME)
    r_fonts.set(qn("w:eastAsia"), FONT_NAME)


def format_paragraph(paragraph, *, align=WD_ALIGN_PARAGRAPH.JUSTIFY, first_line: Cm | None = Cm(1.25), spacing=LINE_SPACING):
    paragraph.alignment = align
    fmt = paragraph.paragraph_format
    fmt.line_spacing = spacing
    fmt.space_before = Pt(0)
    fmt.space_after = Pt(0)
    if first_line is None:
        fmt.first_line_indent = None
    else:
        fmt.first_line_indent = first_line
    return paragraph


def add_body_paragraph(document: Document, text: str):
    paragraph = document.add_paragraph()
    format_paragraph(paragraph)
    set_run_font(paragraph.add_run(text))
    return paragraph


def add_heading(document: Document, text: str):
    paragraph = document.add_paragraph()
    format_paragraph(paragraph, align=WD_ALIGN_PARAGRAPH.CENTER, first_line=Cm(0))
    paragraph.paragraph_format.keep_with_next = True
    set_run_font(paragraph.add_run(text.upper()), bold=True)
    return paragraph


def add_subheading(document: Document, text: str):
    paragraph = document.add_paragraph()
    format_paragraph(paragraph)
    paragraph.paragraph_format.keep_with_next = True
    set_run_font(paragraph.add_run(text), bold=True)
    return paragraph


def add_blank(document: Document):
    paragraph = document.add_paragraph()
    format_paragraph(paragraph, first_line=Cm(0))
    set_run_font(paragraph.add_run(""))
    return paragraph


def add_page_number(paragraph):
    run = paragraph.add_run()
    set_run_font(run)
    fld_begin = OxmlElement("w:fldChar")
    fld_begin.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = " PAGE "
    fld_end = OxmlElement("w:fldChar")
    fld_end.set(qn("w:fldCharType"), "end")
    run._r.append(fld_begin)
    run._r.append(instr)
    run._r.append(fld_end)


def set_page_number_start(section, start: int):
    sect_pr = section._sectPr
    existing = sect_pr.find(qn("w:pgNumType"))
    if existing is not None:
        sect_pr.remove(existing)
    pg_num = OxmlElement("w:pgNumType")
    pg_num.set(qn("w:start"), str(start))
    sect_pr.append(pg_num)


def set_section_margins(section):
    section.top_margin = Mm(20)
    section.bottom_margin = Mm(20)
    section.left_margin = Mm(30)
    section.right_margin = Mm(15)


def toc_line(title: str, page: str | None, *, bold: bool = False) -> str:
    if not page:
        return title
    dots = "." * max(8, 74 - len(title))
    return f"{title}{dots}{page}"


def add_toc_paragraph(document: Document, text: str, *, bold: bool = False):
    paragraph = document.add_paragraph()
    format_paragraph(paragraph, align=WD_ALIGN_PARAGRAPH.LEFT, first_line=Cm(0))
    set_run_font(paragraph.add_run(text), bold=bold)
    return paragraph


